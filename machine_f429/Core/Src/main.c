typedef struct
{
  float temp_inlet;
  float temp_outlet;
  float pressure_bar;
  float motor_rpm;

  uint8_t estop_active;
  uint8_t door_closed;
  uint8_t alarm_active;

  uint32_t uptime_ms;
  uint16_t alarm_code;
} MachineState_t;

MachineState_t g_machine;
#define FRAME_STX 0xAA
#define FRAME_VERSION 0x01

// total frame size = 19 bytes
#define FRAME_SIZE 19

static uint8_t g_tx_frame[FRAME_SIZE];

static uint8_t calc_checksum(const uint8_t *data, uint8_t len)
{
  uint16_t sum = 0;
  for (uint8_t i = 0; i < len; i++)
  {
    sum += data[i];
  }
  return (uint8_t)(sum & 0xFF);
}

static void build_machine_frame(void)
{
  int16_t temp_in_q1  = (int16_t)(g_machine.temp_inlet  * 10.0f);
  int16_t temp_out_q1 = (int16_t)(g_machine.temp_outlet * 10.0f);
  uint16_t press_q2   = (uint16_t)(g_machine.pressure_bar * 100.0f);
  uint16_t rpm_u16    = (uint16_t)(g_machine.motor_rpm);

  uint8_t flags = 0;
  if (g_machine.estop_active) flags |= (1 << 0);
  if (g_machine.door_closed)  flags |= (1 << 1);
  if (g_machine.alarm_active) flags |= (1 << 2);

  uint16_t alarm = g_machine.alarm_code;
  uint32_t uptime = g_machine.uptime_ms;

  uint8_t idx = 0;

  g_tx_frame[idx++] = FRAME_STX;
  g_tx_frame[idx++] = FRAME_SIZE - 3; // LEN = bytes from VERSION to last data (excluding STX, LEN, CHK)
  g_tx_frame[idx++] = FRAME_VERSION;

  // temp_in (int16)
  g_tx_frame[idx++] = (uint8_t)(temp_in_q1 & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((temp_in_q1 >> 8) & 0xFF);

  // temp_out (int16)
  g_tx_frame[idx++] = (uint8_t)(temp_out_q1 & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((temp_out_q1 >> 8) & 0xFF);

  // pressure (uint16)
  g_tx_frame[idx++] = (uint8_t)(press_q2 & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((press_q2 >> 8) & 0xFF);

  // rpm (uint16)
  g_tx_frame[idx++] = (uint8_t)(rpm_u16 & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((rpm_u16 >> 8) & 0xFF);

  // flags (uint8)
  g_tx_frame[idx++] = flags;

  // alarm (uint16)
  g_tx_frame[idx++] = (uint8_t)(alarm & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((alarm >> 8) & 0xFF);

  // uptime (uint32)
  g_tx_frame[idx++] = (uint8_t)(uptime & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((uptime >> 8) & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((uptime >> 16) & 0xFF);
  g_tx_frame[idx++] = (uint8_t)((uptime >> 24) & 0xFF);

  // checksum over all previous bytes (except checksum itself)
  uint8_t chk = calc_checksum(g_tx_frame, FRAME_SIZE - 1);
  g_tx_frame[idx++] = chk;

  // idx should now be FRAME_SIZE
}
static void send_machine_frame(void)
{
  build_machine_frame();
  // change &huart1 if your UART is different
  HAL_UART_Transmit(&huart1, g_tx_frame, FRAME_SIZE, 100);
}

