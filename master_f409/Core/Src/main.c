      int res = parse_frame_full(g_rx_frame, &g_machine_rx);
      if (res != 0)
      {
          // invalid frame (checksum, length, etc.)
          continue;
      }
      /* Forward raw frame through USB CDC */
      CDC_Transmit_FS(g_rx_frame, FRAME_SIZE);

      // 4) Use the decoded values (here: drive LEDs)

      // Example LED mapping (adjust to your pins!):
      // - PD12: door_closed
      // - PD13: estop_active
      // - PD14: alarm_active

      if (g_machine_rx.door_closed)
          HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);
      else
          HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);

