/* ---- Helper: checksum ---- */

static uint8_t calc_checksum(const uint8_t *data, uint8_t len)
{
    uint16_t sum = 0;
    for (uint8_t i = 0; i < len; i++)
    {
        sum += data[i];
    }
    return (uint8_t)(sum & 0xFF);
}
/* ---- Helper: parse full frame into MachineState_t ---- */

static int parse_frame_full(const uint8_t *buf, MachineState_t *m)
{
    // 1) Basic header checks
    if (buf[0] != FRAME_STX)
        return -1;

    if (buf[1] != FRAME_SIZE - 3)   // LEN should be payload length = 16
        return -2;

    if (buf[2] != FRAME_VERSION)
        return -3;

    // 2) Checksum
    uint8_t expected = calc_checksum(buf, FRAME_SIZE - 1);
    uint8_t actual   = buf[FRAME_SIZE - 1];
    if (expected != actual)
        return -4;

    // 3) Unpack fields (little-endian)
    uint8_t idx = 3; // start of payload (after STX, LEN, VERSION)

    int16_t  temp_in_q1  = (int16_t)(buf[idx] | (buf[idx+1] << 8));
    idx += 2;

    int16_t  temp_out_q1 = (int16_t)(buf[idx] | (buf[idx+1] << 8));
    idx += 2;

    uint16_t press_q2    = (uint16_t)(buf[idx] | (buf[idx+1] << 8));
    idx += 2;

    uint16_t rpm_u16     = (uint16_t)(buf[idx] | (buf[idx+1] << 8));
    idx += 2;

    uint8_t  flags       = buf[idx++];

    uint16_t alarm       = (uint16_t)(buf[idx] | (buf[idx+1] << 8));
    idx += 2;

    uint32_t uptime      =  (uint32_t)buf[idx]
                          | ((uint32_t)buf[idx+1] << 8)
                          | ((uint32_t)buf[idx+2] << 16)
                          | ((uint32_t)buf[idx+3] << 24);
    idx += 4;

    // 4) Convert to engineering units
    m->temp_inlet   = (float)temp_in_q1  / 10.0f;
    m->temp_outlet  = (float)temp_out_q1 / 10.0f;
    m->pressure_bar = (float)press_q2    / 100.0f;
    m->motor_rpm    = (float)rpm_u16;

    m->estop_active = (flags & (1 << 0)) ? 1 : 0;
    m->door_closed  = (flags & (1 << 1)) ? 1 : 0;
    m->alarm_active = (flags & (1 << 2)) ? 1 : 0;

    m->alarm_code   = alarm;
    m->uptime_ms    = uptime;

    return 0; // success
}
